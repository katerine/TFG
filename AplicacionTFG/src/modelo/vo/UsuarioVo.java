package modelo.vo;

public class UsuarioVo {
	
	private Integer idUsuario;
	private String nombreUsuario;
	private Integer edadUsuario;
	private String profesionUsuario;
	private Integer telefonoUsuario;
	
	
	public Integer getIdUsuario() {
		return idUsuario;
	}
	
	public void setIdUsuario(Integer idUsuario) {
		this.idUsuario = idUsuario;
	}

	public String getNombreUsuario() {
		return nombreUsuario;
	}
	
	public void setNombreUsuario(String nombreUsuario) {
		this.nombreUsuario = nombreUsuario;
	}
	
	public Integer getEdadUsuario() {
		return edadUsuario;
	}
	
	public void setEdadUsuario(Integer edadUsuario) {
		this.edadUsuario = edadUsuario;
	}
	
	public String getProfesionUsuario() {
		return profesionUsuario;
	}
	
	public void setProfesionUsuario(String profesionUsuario) {
		this.profesionUsuario = profesionUsuario;
	}
	
	public Integer getTelefonoUsuario() {
		return telefonoUsuario;
	}
	
	public void setTelefonoUsuario(Integer telefonoUsuario) {
		this.telefonoUsuario = telefonoUsuario;
	}
	

}
